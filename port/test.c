void display(void) {
    uint8_t y, x;
    for (y = 0; y < SCREEN_Y / AXIS_Y_STORAGE; y++) {
        JOY_OLED_data_start(y);
        for (x = 0; x < SCREEN_X; x++) {
            JOY_OLED_send(image_data[y * 128 + x]);
        }
        JOY_OLED_end();
    }
    // OLED_fill(0x80);
    _OLED_refresh();
}

// sleep() not in MSVC
#ifdef _MSC_VER
#include <windows.h>
void time_sleep(double s) { Sleep(s * 1000); }
#else
#include <unistd.h>
void time_sleep(unsigned int s) { sleep(s); }
#endif

int main(void) {
    JOY_init();
    while (true) {
        display();
        time_sleep(1u);
    }
    return 0;
}
